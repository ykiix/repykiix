import turtle as t
t.shape('turtle')

t.begin_fill()
t.color('yellow')
t.circle(100)
t.end_fill()

t.penup()
t.left(100)
t.forward(140)
t.pendown()
t.begin_fill()
t.color('blue')
t.circle(15)
t.end_fill()

t.penup()
t.right(115)
t.forward(60)
t.pendown()
t.begin_fill()
t.color('blue')
t.circle(15)
t.end_fill()

t.width(5)
t.penup()
t.color('black')
t.right(130)
t.forward(40)
t.pendown()
t.left(55)
t.forward(30)

t.penup()
t.color('red')
t.forward(30)
t.right(90)
t.forward(35)
t.left(135)
t.pendown()
t.circle(50, 90)

t.mainloop()
